ALL_HINTS = {**HINTS, **DEMO_HINTS}


async def main():
    async with AsyncSessionLocal() as db:
        result = await db.execute(select(Tool))
        tools = result.scalars().all()

        updated = 0
        for tool in tools:
            hint = ALL_HINTS.get(tool.name)
            if hint and not tool.trigger_hint:
                tool.trigger_hint = hint
                updated += 1
                print(f"  ✓ {tool.name}")

        await db.commit()
        print(f"\n✅ Обновлено {updated} инструментов")


if __name__ == "__main__":
    asyncio.run(main())
